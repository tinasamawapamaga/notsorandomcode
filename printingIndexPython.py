#Printing even and odd characters separately in Python
def main():
    i=0
    wordlist=[]
    num=int(input())
    for i in range (0,num):
          word=input()
          wordlist.append(word)
    for x in wordlist:
     if len(x)>1 and len(x)<10001:
        for element in range(0, len(x),2): 
            print(x[element], end="")
        print(" ", end="")     
        for element in range(1, len(x),2): 
            print(x[element], end="")
     print("")   
main()
